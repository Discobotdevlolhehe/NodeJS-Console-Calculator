function sin(angle) {
    return Math.sin(angle);
}

function cos(angle) {
    return Math.cos(angle);
}

function tan(angle) {
    return Math.tan(angle);
}

function cosec(angle) {
    return 1/Math.sin(angle);
}

function sec(angle) {
    return 1/Math.cos(angle);
}

function cot(angle) {
    return 1/Math.tan(angle);
}

export {
    sin,
    cos,
    tan,
    cosec,
    sec,
    cot
}